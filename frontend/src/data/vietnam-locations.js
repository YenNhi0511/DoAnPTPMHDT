// Helper functions
export const getProvinces = () => vietnamLocations.map(loc => loc.province);

export const getDistrictsByProvince = (province) => {
  const location = vietnamLocations.find(loc => loc.province === province);
  return location ? location.districts : [];
};

export const getAllDistricts = () => {
  return vietnamLocations.flatMap(loc => 
    loc.districts.map(district => ({
      district,
      province: loc.province
    }))
  );
};

